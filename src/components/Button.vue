<template>
        <button 
            :class="['button', 'is-outlined', 'is-fullwidth', isLoading]"
            @click="event"
        >
            {{ text }}
        </button>
</template>

<script>
export default {
    name: 'Button',
    
    props: {
        text: { 
            type: String,
            required: true,
        },
        event: {
            type: Function,
            required: true,
        },
        loading: {
            type: Boolean,
            default: false,
        }
    },

    computed: {
        isLoading() {
            return this.loading ? 'is-loading' : ''
        },
    },
}
</script>

